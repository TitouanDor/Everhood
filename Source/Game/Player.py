class Player:
    def __init__(self,position, pv, name, sprite) -> None:
        self.position = position
        self.pv = pv
        self.name = name
        self.x = 0
        self.y = 0
        self.sprite = sprite

        def getName(self): 
            return self.name

        def degat(self, d):
            self.pv -= d


        

    
